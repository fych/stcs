    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        Object id;
        int row = jTable1.getSelectedRow();
        if (row >= jTable1.getModel().getRowCount()||row==-1) {//没有选择行
            JOptionPane.showMessageDialog(this, "请选择你要操作的数据", "警告", JOptionPane.WARNING_MESSAGE);
        } else {
            int column = jTable1.getColumn("会员id").getModelIndex();
            id = (Object)jTable1.getValueAt(row,column);
            Main.memberUpdate = new MemberUpdateUI((String)id);
            Main.memberUpdate.setVisible(true);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

