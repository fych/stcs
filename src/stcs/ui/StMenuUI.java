    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        Object id;
        int row = jTable1.getSelectedRow();
        if (row >= jTable1.getModel().getColumnCount()||row==-1){ //没有选择行
            JOptionPane.showMessageDialog(this,"请选择你要的操作","警告",JOptionPane.WARNING_MESSAGE);
        } else {
            int column = jTable1.getColumn("会员id").getModelIndex();
            id = (Object)jTable1.getValueAt(row, column);
            int i = JOptionPane.showConfirmDialog(this,"您确定删除吗？","询问",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE,null);
            if (i==0) { //确定删除
                stcs.bo.MemberBo bo = new stcs.bo.MemberBo();
                boolean rtn = bo.doDelete((String)id);
                if (rtn) {
                    JOptionPane.showMessageDialog(this,"删除成功","提示",JOptionPane.INFORMATION_MESSAGE);
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(new javax.swing.JFrame(),"删除失败","错误",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

