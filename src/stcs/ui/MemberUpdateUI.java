    public void findMember() {
        stcs.bo.MemberBo bo = new stcs.bo.MemberBo();
        stcs.vo.MemberVo vo = bo.findMemberByM_id(m_id);
        jTextField1.setText(vo.getM_name());
        jComboBox1.setSelectedItem(vo.getM_sex());
        jTextField3.setText(vo.getM_dorm());
        jTextField4.setText(vo.getM_phone());
        jTextField5.setText(vo.getM_email());
        jTextField6.setText(vo.getM_qq());
        jTextField7.setText(vo.getM_dept());
        jTextField8.setText(vo.getM_post());
        jComboBox2.setSelectedItem(vo.getM_state());
        state = vo.getM_state();
        jTextField11.setText(vo.getM_leavetime());
        jTextArea1.setText(vo.getM_desc());
        String money = vo.getM_money();
        if ((money.indexOf("年已交"))!=1){
            jComboBox3.setSelectedItem("已交");
            int i = money.indexOf("年已交");
            jTextField2.setText(money.substring(0, i));
        } else {
            jComboBox3.setSelectedItem(money);
        }
        jTextField9.setText(vo.getM_major());
    }

