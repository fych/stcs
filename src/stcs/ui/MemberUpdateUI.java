    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(this, "请输入姓名！", "警告", JOptionPane.WARNING_MESSAGE);
            jTextField1.requestFocus();
        } else {
            stcs.vo.MemberVo vo = new stcs.vo.MemberVo();
            vo.setM_name(jTextField1.getText());
            vo.setM_sex((String)jComboBox1.getSelectedItem());
            vo.setM_dorm(jTextField3.getText());
            vo.setM_phone(jTextField4.getText());
            vo.setM_qq(jTextField6.getText());
            vo.setM_email(jTextField5.getText());
            vo.setM_dept(jTextField7.getText());
            vo.setM_post(jTextField8.getText());
            vo.setM_state((String)jComboBox2.getSelectedItem());
            //组合缴费情况
            String money;
            if("已交".equals((String)jComboBox3.getSelectedItem())) {
                money = jTextField2.getText() + "年已交";
            } else {
                money = (String)jComboBox3.getSelectedItem();
            }
            vo.setM_money(money);
            vo.setM_leavetime(jTextField11.getText());
            vo.setM_major(jTextField9.getText());
            vo.setM_desc(jTextArea1.getText());
            vo.setM_id(m_id);
            
            stcs.bo.MemberBo bo = new stcs.bo.MemberBo();
            boolean rtn = bo.doUpdate(vo);
            if (rtn) {
                JOptionPane.showMessageDialog(this,"修改成功！","提示",JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                Main.memberUpdate = null;
                Main.stMenu.loadData();
            } else {
                JOptionPane.showMessageDialog(new javax.swing.JFrame(),"修改失败！","错误",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

